/*
Operadores de comparação

> maior que
>= maior que ou igual a
< menor que
<= menor ou igual a
== igualdade (valor) ****
=== igualdade estrita (valor e tipo)
!= diferente (valor)
!== diferente estrito (valor e tipo)
*/
console.log(10 > 5);
console.log(5 < 10);
console.log(10 <= 10);
console.log(10 >= 10);
console.log(7 == '7'); // nao é ideal usar, nao verifica o tipo
console.log(7 === '7'); // verifica o tipo
console.log(7 != '7'); //nao é ideal usar, nao verifica o tipo
console.log(7 !== '7'); // verifica o tipo